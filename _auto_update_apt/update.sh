#!/bin/bash

# Скрипт для обновления всех программ в Ubuntu
# Запускать с правами суперпользователя

echo "Начинаю обновление системы..."

# Обновление списка пакетов
echo "Обновление списка пакетов..."
apt update

# Обновление установленных пакетов
echo "Обновление установленных пакетов..."
apt upgrade -y

# Обновление дистрибутива (если доступно)
echo "Проверка обновлений дистрибутива..."
apt dist-upgrade -y

# Удаление ненужных пакетов
echo "Очистка системы..."
apt autoremove -y
apt autoclean

echo "Обновление завершено!"